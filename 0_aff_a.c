#include <unistd.h>

void	pc(char c )
{
	write(1, &c, 1);
}

int	main(int ac, char **av)
{
	if (ac != 2)
	{
		pc('a');
		pc('\n');
		return (0);	
	}
	else
	{
		while (*av[1])
		{
			if (*av[1] == 'a')
			{
				pc('a');
				pc('\n');
				return (0);
			}
			av[1]++;
		}
		pc('\n');
	}
	return (0);
}
