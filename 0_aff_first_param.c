#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

int	main(int ac, char **av)
{
	if (ac < 2)
	{
		ft_putchar('\n');
		return (0);
	}
	else
	{
		while (*av[1])
		{
			ft_putchar(*av[1]);
			(av[1])++;
		}
		ft_putchar('\n');
	}
	return (0);
}
