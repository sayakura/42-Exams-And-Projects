#include <unistd.h>

char ht[127];
void	pc(char c)
{
	write(1, &c, 1);
}

int	main(int ac, char **av)
{
	if (ac != 3)
	{
		pc('\n');
		return (0);
	}
	else
	{
		int i = 0;
		while (av[2][i])
		{
			ht[(int)av[2][i]] = 1;
			i++;
		}
		i = 0;
		while (av[1][i])
		{
			if (ht[(int)av[1][i]])
				pc(av[1][i]);
			ht[(int)av[1][i]] = 0;
			i++;
		}
		pc('\n');
	}
	return (0);
}
