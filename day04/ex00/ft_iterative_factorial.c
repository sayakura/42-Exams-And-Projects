int	ft_iterative_factorial(int nb)
{
	int num;
	int i;

	if (nb == 0 || nb == 1)
		return (1);
	if (nb < 0 || nb > 12)
		return (0);
	num = 1;
	i = 1;
	while (--nb)
		num *= ++i;
	return (num);
}
