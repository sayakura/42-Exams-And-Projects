char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char *ptr;

	if ((ptr = dest) == 0)
		return (char *)(0);
	while (n-- && (*ptr++ = *src++) != 0)
		;
	*ptr = '\0';
	return (dest);
}
