int	ft_strcmp(char *s1, char *s2)
{
	while (*s1 == *s2++)
	{
		if (*s1 == '\0')
			return (0);
		s1++;
	}
	if (*s1 == *s2)
		return (0);
	else if (*(unsigned char *)s1 > *(unsigned char *)s2)
		return (1);
	else
		return (-1);
}
