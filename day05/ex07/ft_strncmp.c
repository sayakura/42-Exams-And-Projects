int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	while (n--)
	{
		if (*s1 != *s2)
			if (*(unsigned char *)s1 < *(unsigned char *)s2)
				return (-1);
			else
				return (1);
		else if (*s1 == '\0')
			return (0);
		s1++;
		s2++;
	}
	return (0);
}
